# 回答例
A, B = map(int, input().split()) # 入力を整数型として受け取る
if A>B:
    print(A)
else:
    print(B)

# 解説
"""
このコードは非常にシンプルで、AとBを比較し、大きい方を出力します。
AがBより大きい場合、Aを出力し、そうでなければ（AがB以下の場合）Bを出力します。
このコードは、AとBが等しい場合にもBを出力します。
"""

# カオル氏が書いたコード
A, B = map(int, input().split())

if A > B:
    print(A)
elif A < B:
    print(B)
else:
    pass

"""
カオル氏のコードも基本的には同じタスクを実行しますが、
AとBが等しい場合にpassを使って何もしないという分岐があります。
これは実際には必要ない操作です。
AとBが等しい場合、何も出力しないわけではなく、どちらかを出力するべきです
（問題の要件にもよりますが、一般的にはどちらか一方を出力します）。
したがって、else節はこのコンテキストでは不要です。
"""
A, B = map(int, input().split())

if A > B:
    print(A)
elif A < B:
    print(B)
